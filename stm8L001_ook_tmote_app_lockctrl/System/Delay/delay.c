#include "delay.h"

/**********************************************************************************************************
 @Function			void Delay_US_Normal(void)
 @Description			Delay_US_Normal               : 16M System Clock
 @Input				void
 @Return				void
**********************************************************************************************************/
void Delay_US_Normal(void)
{
     asm("nop");
     asm("nop");
     asm("nop");
     asm("nop");
}

/**********************************************************************************************************
 @Function			void Delay_MS_Normal(u16 nCount)
 @Description			Delay_MS_Normal               : 16M System Clock
 @Input				nCount                        : MS
 @Return				void
**********************************************************************************************************/
void Delay_MS_Normal(u16 nCount)
{
     while (nCount--) {
          for (u16 i = 900; i > 0; i--) Delay_US_Normal();
     }
}

/**********************************************************************************************************
 @Function			void Delay_US_FuncType1(u16 nCount)
 @Description			Delay_US_FuncType1            : 16M System Clock
 @Input				nCount                        : US
 @Return				void
**********************************************************************************************************/
void Delay_US_FuncType1(u16 nCount)
{
     while (nCount--) {
          for (u16 i = 50; i > 0; i--) Delay_US_Normal();
     }
}




















